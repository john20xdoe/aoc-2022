const rules = {
  them: {
    A: 1,
    B: 2,
    C: 3
  },
  me: {
    X: 1,
    Y: 2,
    Z: 3
  }
};

type Them = keyof typeof rules['them'];
type Me = keyof typeof rules['me'];
type Rule = { mode?: keyof typeof rules; them: Them; me: Me };

const outscore = ({ them, me }: Rule) =>
  Number(rules.me[me] > rules.them[them] ? 6 : rules.me[me] < rules.them[them] ? 0 : 3);

const score = ({ them, me }: Rule) => {
  const myScore = rules.me[me] + outscore({ them, me });
  // const theirScore = rules.them[them];
  return { myScore };
};

const parsed = select(
  test.split('\n'),
  (line) => {
    const [them1, me1] = line.split(' ');

    return { them: them1 as Them, me: me1 as Me };
  },
  (c) => true
);

const scores = sum(
  select(
    parsed,
    ({ them, me }) => score({ them, me }),
    (c) => true
  )
);

